import math
co = float(input('Digite o valor do cateto oposto (menor):'))
ca = float(input('Digite o valor do cateto adjacente (maior):'))
hip = math.hypot(co, ca)
print('O valor da hipotenusa é: {:.2f}'.format(hip))

